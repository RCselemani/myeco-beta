    <p> Un soin particulier a été apporté à la conception et à la fabrication de votre montre My Eco Montre , 
      qui a été conçue et fabriquée avec un soin extrême. Les recommandations qui suivent vous aideront à maintenir 
      votre montre en parfait état de marche pendant de nombreuses années.</p>
                                       
       <h3> Fréquence de révision </h3>
          <p> Votre My Eco Montre est un instrument de haute précision qui fonctionne 24 heures par jour et qui exige
             un entretien régulier afin de maintenir sa précision et son étanchéité à long terme.La fréquence des révisions peut varier 
             selon l’utilisation qui est faite de la montre et l’environnement dans lequel celle-ci est portée.
            Par exemple, l’étanchéité peut être détériorée par le vieillissement naturel des joints, mais aussi par l’utilisation régulière de la couronne de remontage, 
            par les chocs et d’autres facteurs tels que la transpiration, l’usage de cosmétiques ou les variations de température. 
            Nous recommandons d’effectuer chaque année une vérification de l’étanchéité et de procéder tous les 3 à 5 ans à une révision auprès 
            d’un de nos centres de service après-vente. </p>

         <h3>Étanchéité</h3>
            <p> À l’exception de quelques modèles spécifiques, en particulier ceux d’époque, les montres My Eco Montre  sont étanches 
              jusqu’à au moins 30 mètres. Si l’étanchéité est supérieure à 30 mètres, la profondeur maximale est indiquée sur le fond du 
              boîtier.  Lorsqu’une montre est étanche, son mouvement est protégé de la poussière, de l’humidité et de tout risque 
              de détérioration en cas d’immersion. Ainsi, votre montre est étanche à l’eau dans toutes les circonstances courantes de 
              la vie quotidienne, que ce soit sous la pluie, sous la douche ou lors d’une baignade. Néanmoins, vous devriez éviter d’exposer votre montre 
              à des jets d’eau de forte pression (provoqués par exemple par des appareils de nettoyage de voiture ou de jardin), et également la préserver 
              des chocs pouvant survenir lors de la pratique de sports nautiques violents (jet-ski, etc.).
              Veuillez noter également que l’étanchéité de la montre n’est pas garantie à vie. Les joints qui assurent l’étanchéité 
              peuvent se détériorer en raison du vieillissement naturel, de l’utilisation régulière de la couronne, de chocs ou d’autres facteurs 
              tels que la transpiration, l’usage de cosmétiques ou les variations de température.
              C’est pourquoi nous vous recommandons de faire vérifier l’étanchéité chaque année, 
              de préférence avant les vacances estivales.</p>

          <h3>Avertissement</h3>
            <p> Lorsque votre montre est immergée dans l’eau, veillez à ce que la couronne soit bien en place 
              (poussée ou vissée selon le modèle), et évitez d’appuyer sur les poussoirs car de l’eau pourrait ainsi s’introduire à 
              l’intérieur du boîtier. Si vous remarquez la moindre trace de condensation à l’intérieur de la montre, 
              vous devez aussitôt la confier à l’un de nos revendeurs agréés ou à l’un de nos centres de service après-vente. </p>
    
          <h3>Remarque </h3> 
              <ul>
                <ol>
                   <li>L’efficacité du remontage automatique est directement liée au mouvement du poignet. 
                     En cas d’activité insuffisante, la montre peut s’arrêter tout en étant portée. Par mesure de précaution, 
                     nous conseillons dans ce cas de remonter la montre manuellement en tournant la couronne une vingtaine de fois.
                     De même, le bracelet doit être fermement attaché au poignet : s’il est trop large, la montre pourrait 
                     ne pas se remonter correctement.</li> 
                    <li>Il est nécessaire de retirer la montre du poignet avant de la remonter. Ceci permet d’assurer que la tige de remontoir reste bien droite lors du remontage, ce qui évite d’endommager la couronne ou le mouvement.</li>   
                    <li>Ne pas secouer délibérément une montre à remontage automatique pour la remonter plus vite. Cela est inutile et pourrait endommager le mouvement.</li> 
                    <li>Même si votre montre reste plusieurs mois sans être portée, nous vous conseillons de la remonter à la main régulièrement. Cela empêchera les huiles de se figer ou de se répandre dans le mouvement.</li>  
                </ol>
               </ul>
          <h3>Couronne</h3>
            <p> Après avoir utilisé la couronne, veillez à ce que celle-ci soit bien en place (poussée ou vissée, selon le modèle) 
              pour la protéger de l’humidité et empêcher l’eau de pénétrer à l'intérieur du boîtier. </p>
        
          <h3>TChocs et vibrations </h3>
            <p> Toutes les montres Baume & Mercier sont testées conformément aux normes officielles de l'industrie horlogère suisse
               et peuvent résister à des chocs allant jusqu’à 5 000g. Toutefois, les impacts violents ainsi que les vibrations 
               fréquentes et répétées sont à proscrire.</p>
                                
          <h3> Remplacement de la pile</h3>
            <p> La durée de vie de la pile est généralement de 3 à 6 ans, selon le modèle. La plupart de nos montres à quartz dotées 
              d’une aiguille centrale des secondes sont équipées d’un système qui indique l’épuisement imminent de la pile environ 
              deux semaines avant : lorsque l’aiguille centrale des secondes commence à sauter toutes les quatre secondes, cela signifie 
              qu’il est temps de remplacer la pile. Il ne faut jamais laisser une pile usée à l’intérieur d’une montre afin d’éviter tout risque de fuite susceptible d’endommager le mouvement.
              Nous recommandons de faire remplacer la pile par un horloger certifié qui en profitera pour vérifier l’étanchéité 
              de la montre et l’état du mouvement. </p>
        
          <h3> Température </h3>
             <p> Ne pas exposer la montre à des températures extrêmes (supérieures à 60ºC ou inférieures à 0ºC) 
               ni à des variations brutales de température. </p>
        
          <h3> Nettoyage</h3>
             <p> Pour préserver l’éclat de votre boîtier étanche et de votre bracelet en métal, utilisez 
               une brosse à dents souple avec de l’eau chaude savonneuse. Lorsque vous avez terminé, 
               rincez la montre à l’eau claire et essuyez-la avec un chiffon doux. Il est recommandé de nettoyer la montre de 
               la même manière après avoir nagé dans une piscine ou dans la mer.
               Avant de nettoyer votre montre, veillez à ce que la couronne soit bien en place (poussée ou vissée, selon le modèle). </p>
        
          <h3> Bracelets </h3>
             <p>
               <h4>Bracelet en cuir </h4>
               La longévité d’un bracelet en cuir dépend de ses conditions d’utilisation et de la façon dont il est porté.
                Par exemple, un bracelet est altéré par l’acidité de la peau, la transpiration, les variations de température, l’ensoleillement prolongé, les crèmes pour la peau, les cosmétiques...
                Des conditions extrêmes telles que les activités sportives ou la plongée sous-marine réduisent considérablement sa longévité.
                Si vous souhaitez commander un bracelet en cuir, merci de cliquer ci dessous : 
                          
                <h4>Bracelet en caoutchouc</h4>
                La longévité d’un bracelet en caoutchouc dépend de ses conditions d’utilisation et de la façon dont il est porté.
                Pour conserver votre bracelet en caoutchouc plusieurs années, nous vous conseillons d’éviter le contact avec 
                les huiles et les cosmétiques, et d’éviter tout ensoleillement prolongé. De même, évitez de frotter votre bracelet 
                contre vos vêtements car cela pourrait entraîner sa décoloration (a fortiori pour un bracelet blanc) ou sa détérioration 
                prématurée. Par ailleurs, veuillez noter que vous pouvez nettoyer votre bracelet en caoutchouc avec de l’eau savonneuse.
