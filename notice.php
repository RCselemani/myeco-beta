                <h4>Nacre</h4>
                 Il est recommandé de nettoyer délicatement la nacre avec un chiffon doux et sec ou à l’eau claire. Laissez la montre
                sécher complètement avant de la ranger. Il est préférable d’éviter de ne pas mettre la nacre en contact avec du parfum
                 ou une lotion corporelle, ce qui pourrait altérer ses reflets iridescents. Ainsi, après avoir mis du parfum ou une lotion corporelle, il est recommandé de patienter environ 10 minutes avant de porter votre montre. Enfin, votre montre ornée de nacre sera mieux protégée si elle est conservée dans un sac en coton doux.
                          
                 <h4>Bronze</h4>
                  Le bronze est un matériau qui vieillit en fonction du mode de vie de celui ou celle qui le porte. 
                  Quand le bronze entre en contact avec des éléments corrosifs – notamment la transpiration et l’eau de mer – cet alliage 
                  adopte une patine vert-de-gris qui rend chaque exemplaire unique.
